print("Este es un programa que calcula el area y perimetro de un rectangulo")
Lrec= float(input("Introduce el Ancho del rectangulo"))
Hrec= float(input("Introduce la altura del rectangulo"))
Perimetro = 2 * Lrec + 2 * Hrec  # Fórmula del perímetro
